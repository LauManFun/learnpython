import math

a,b,c = input("请输入一元二次方程的系数a、b、c，用空格分开：").split()
a,b,c = float(a),float(b),float(c)

d = b**2 - (4 * a * c)      # 两个星号“**”表示幂运算，即“b**2”等价于“b*b”

if d<0:
    print("方程没有实数根")
elif d>0:
    x1 = (-b + d**0.5)/(2 * a)
    x2 = (-b - d**0.5)/(2 * a)
    print("方程有两个实数根：%.2f和%.2f" % (x1,x2))
else:
    x = (-b)/(2 * a)
    print("方程只有一个实数根：%.2f" % x)

